package com.example.h2jdbc.entities;

public class Address {

    private final String street;
    private String city;

    public Address(String street, String city) {
        this.street = street;
        this.city = city;
    }

    public String getStreet() {
        return street;
    }

    public String getCity() {
        return city;
    }

    @Override
    public String toString() {
        return " " + street + " " + city;
    }
}
